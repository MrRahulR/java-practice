package com.qacaffe.examples.loops;

public class DoWhile1 {

    public static void main(String[] args) {
        System.out.println("Example of do-while Loop.");
        int j = 1;
        do {
            System.out.println(j);
            j++;
        } while (j < 10);
    }
}

/*
Output:
Example of do-while Loop.
1
2
3
4
5
6
7
8
9
 */